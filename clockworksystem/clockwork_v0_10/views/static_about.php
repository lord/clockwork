<p>What is Clockwork?</p>
<p>Clockwork puts online highscores and statistics into your game maker games!</p>

<p>What game development platforms does Clockwork work on?</p>
<p>Right now, only Game Maker 8 Pro.  We hope to add other platforms (XNA, Unity) sometime in the distant future.</p>

<p>How do I get started with clockwork?</p>
<p>Just <a href='<?php echo site_url("join"); ?>'>sign up for an account</a>, and then download the clockwork SDK. Check out the <a href='<?php site_url("help/gettingstarted"); ?>'>getting started tutorial</a> for more information.</p>

