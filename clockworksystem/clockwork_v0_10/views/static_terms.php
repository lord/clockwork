<h2>1. Data</h2>
<p>When creating an application with Clockwork, you might send data to Clockwork including, but not limited to, number of game starts and average FPS.  By sending this data, or by creating an application which sends this data, you agree that you have the legal rights to give Clockwork this information.</p>

<h2>2. Account</h2>
<p>It is your responsibility to keep your account password and game security keys safe.  You are responsible and liable for any activity that occurs under your account, or under the games registered by your account.</p>

<h2>3. Spamming</h2>
<p>If a game under your account is the subject of large amounts of data coming in from one ip address, Clockwork may change your security key and/or ban the ip address submitting the spam.</p>

<h2>4. Suspension of Account</h2>
<p>Clockwork may suspend your account if it detects that the account is being used for malicious intent.</p>
